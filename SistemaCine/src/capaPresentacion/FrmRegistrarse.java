    private void cargarDepartamentos() {
        try {
            clsDepartamento depto = new clsDepartamento();
            ResultSet rs = depto.obtenerDepartamentos();

            cboDepartamento.removeAllItems();  // Limpiar el ComboBox
            mapDepartamentos.clear();  // Limpiar el mapa

            while (rs.next()) {
                String nombreDepartamento = rs.getString("nombre");
                int idDepartamento = rs.getInt("idDpto");

                // Agregar el nombre al ComboBox
                cboDepartamento.addItem(nombreDepartamento);

                // Agregar el nombre y el ID al mapa
                mapDepartamentos.put(nombreDepartamento, idDepartamento);
            }
            rs.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al cargar departamentos: " + ex.getMessage());
        }
    }

    private void cargarProvincias(int idDpto) {
        cboProvincia.removeAllItems();  // Limpiar el ComboBox
        mapProvincias.clear();  // Limpiar el mapa

        try {
            clsProvincia prov = new clsProvincia();
            ResultSet rs = prov.obtenerProvinciasPorDepartamento(idDpto);
            while (rs.next()) {
                String nombreProvincia = rs.getString("nombre");
                int idProvincia = rs.getInt("idProvincia");

                // Agregar el nombre al ComboBox
                cboProvincia.addItem(nombreProvincia);

                // Agregar el nombre y el ID al mapa
                mapProvincias.put(nombreProvincia, idProvincia);
            }
            rs.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al cargar provincias: " + ex.getMessage());
        }
    }

    private void cargarDistritos(int idProvincia) {
        cboDistrito.removeAllItems();  // Limpiar el ComboBox
        mapDistritos.clear();  // Limpiar el mapa

        try {
            clsDistrito dist = new clsDistrito();
            ResultSet rs = dist.obtenerDistritosPorProvincia(idProvincia);
            while (rs.next()) {
                String nombreDistrito = rs.getString("nombre");
                int idDistrito = rs.getInt("idDistrito");

                // Agregar el nombre al ComboBox
                cboDistrito.addItem(nombreDistrito);

                // Agregar el nombre y el ID al mapa
                mapDistritos.put(nombreDistrito, idDistrito);
            }
            rs.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al cargar distritos: " + ex.getMessage());
        }
    }

