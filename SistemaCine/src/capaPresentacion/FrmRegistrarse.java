    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        try {
            String nombre = txtNombre.getText();
            String apellidos = txtApellido.getText();
            String email = txtEmail.getText();
            String celular = txtCelular.getText();
            String contraseña = txtContraseña.getText();
            String tipoDocIdentidad = (String) cboTipoDoc.getSelectedItem(); // Si es un ComboBox
            String nroDocIdentidad = txtNroDoc.getText();
            int idDistrito = mapDistritos.get((String) cboDistrito.getSelectedItem()); // Map del distrito seleccionado
            String sexo = rbHombre.isSelected() ? "HOMBRE" : "MUJER"; // Radiobutton para seleccionar sexo
            Date fechaNac = jcFechaNac.getDate(); // Obtener la fecha desde el date chooser

            // Instancia de la clase clsUsuario
            clsUsuario usuario = new clsUsuario();

            // Llamar al método para registrar al usuario
            usuario.registrarUsuario(nombre, apellidos, email, celular, contraseña, tipoDocIdentidad, nroDocIdentidad,
                    idDistrito, sexo, fechaNac);

            JOptionPane.showMessageDialog(null, "Usuario registrado correctamente");
            this.dispose();

            FrmIniciarSesion objInicio = new FrmIniciarSesion();
            objInicio.setVisible(true);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al registrar el usuario: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarseActionPerformed

